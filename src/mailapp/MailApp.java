    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        SendMail sendMail = new SendMail();
        if(jCheckBox2.isSelected())
        {
            sendMail.sendPGP(jTextField1.getText(), jTextField2.getText(), jTextArea1.getText(),file, usr, pass,jTextField3.getText());
        }
        else 
        {
        sendMail.send(jTextField1.getText(), jTextField2.getText(), jTextArea1.getText(),file, usr, pass);
        }
        file = null;
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        JFileChooser chooser = new JFileChooser();
        int choose = -1;
        chooser.setDialogTitle("Select a file");
        choose = chooser.showOpenDialog(null);
        if (choose == JFileChooser.APPROVE_OPTION) {
            file = chooser.getSelectedFile().getAbsolutePath();
        }
    }//GEN-LAST:event_jButton2ActionPerformed

